package jdr.appli.model.character;

public class Saves {
	
	private int vigor;
	private int reflex;
	private int will;
	
	public int getVigor() {
		return vigor;
	}
	
	public void setVigor(int vigor) {
		this.vigor = vigor;
	}
	
	public int getReflex() {
		return reflex;
	}
	
	public void setReflex(int reflex) {
		this.reflex = reflex;
	}
	
	public int getWill() {
		return will;
	}
	
	public void setWill(int will) {
		this.will = will;
	}	

}
